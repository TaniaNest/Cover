public class Second {


    private float c;
    private float d;

    Second() {
    }

    Second(float c, float d) {
        this.c = c;
        this.d = d;
    }

    public float getC() {
        return c;
    }

    public void setC(float c) {
        this.c = c;
    }

    public float getD() {
        return d;
    }

    public void setD(float d) {
        this.d = d;
    }
}
